# For each expression, write the value and type(of the value of the expression).

width = 17
height = 12.0

print(type(width//2))   # 8 int
print(width//2)

print(type(width/2.0))  # 8.5 float
print(width/2.0)

print(type(height/3))   # 4.0(or close to) float
print(height/3)

print(type(1 + 2 * 5))        # 11 int
print(1 + 2 * 5)
